"""Golden characterization tests for the pokemon MCTS feature encoding and model.

These pin the CURRENT behavior of `agents.mcts` (feature encoding, model state-dict
keys, feature-layout constants) so the mcts package refactor can prove it changes
nothing.

The golden data in tests/golden/pokemon_mcts_encoding.json was generated by running
this file directly (see the __main__ block): one live game was driven once with the
abomasnow deck and a scripted first-maxCount-options policy, and the raw observation
dicts recorded at several decision points were frozen into the JSON together with the
exact encoder/decoder outputs computed by the then-current code. The tests replay the
frozen observations only — no live game — so they are deterministic by construction
(the compiled cg engine shuffles from /dev/urandom and cannot be seeded).

Regenerate (ONLY when the encoding is intentionally changed):
    uv run python tests/test_pokemon_mcts_golden.py
"""

