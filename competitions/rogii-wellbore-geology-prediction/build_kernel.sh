#!/usr/bin/env bash
# Assemble the single-file Kaggle inference kernel.
# Kaggle script kernels are single-file, so we concatenate the (verbatim,
# tested) feature library with the inference entrypoint.
set -euo pipefail
cd "$(dirname "$0")"
{
  echo "# AUTO-GENERATED by build_kernel.sh — do not edit. Edit rogii_features.py / _inference_main.py."
  cat rogii_features.py
  cat _inference_main.py
} > inference_kernel.py
echo "Wrote inference_kernel.py ($(wc -l < inference_kernel.py) lines)"
